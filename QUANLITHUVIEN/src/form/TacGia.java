        private void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblTacgia.getModel();
        model.setRowCount(0);
        try {
            List<TacGiaEn> list = tgdao.selectAll();
                    
            for (TacGiaEn nv : list) {
                Object[] row = {
                    nv.getMaTG(),
                    nv.getTenTG(),
                    };
                model.addRow(row);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi truy vấn dữ liệu nhân viên!");
            e.printStackTrace();
        }
    }
      
        
