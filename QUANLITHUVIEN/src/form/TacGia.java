    private void delete() {

        String manv = txtMaTg.getText();
        if (MsgBox.confirm(this, "Bạn có thực sự muốn xóa tác giả này không?")) {
            try {
                tgdao.delete(manv);
                this.fillTable();
                this.clear1();
                MsgBox.alert(this, "Xóa thành công!");
            } catch (Exception e) {
                MsgBox.alert(this, "Xóa thất bại!");
                e.printStackTrace();
            }
        }

    }
    
