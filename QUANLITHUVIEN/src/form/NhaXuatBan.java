    private void delete() {

        String manv = txtMaNXB.getText();
        if (MsgBox.confirm(this, "Bạn có thực sự muốn xóa nhân viên này không?")) {
            try {
                nxbdao.delete(manv);
                this.fillTable();
                this.clear1();
                MsgBox.alert(this, "Xóa nhân viên thành công!");
            } catch (Exception e) {
                MsgBox.alert(this, "Xóa nhân viên thất bại!");
                e.printStackTrace();
            }
        }

    }
    
