 private void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblNXB.getModel();
        model.setRowCount(0);
        try {
            List<NXB> list = nxbdao.selectAll();
                    
            for (NXB nv : list) {
                Object[] row = {
                    nv.getMaNXB(),
                    nv.getTenNXB(),
                    };
                model.addRow(row);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi truy vấn dữ liệu nhân viên!");
            e.printStackTrace();
        }
    }
 
 
 
 
